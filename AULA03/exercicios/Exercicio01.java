package exercicios;

public class Exercicio01 {
    public static void main(String[] args) {
        int cont;

        cont = 100;
        while (cont <= 200){
            System.out.print(cont + " "); //se tirar o ln do print ele não faz lista, coloca um numero ao lado do outro 
            cont ++; //na linha de cima colocando o + " " no print do cont ele dá um espaço entre os nºs
        }
    }
    
}
